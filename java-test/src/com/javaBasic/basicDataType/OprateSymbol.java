package com.javaBasic.basicDataType;

/**
 * OprateSymbol
 */
public class OprateSymbol {

    public static void main(String[] args) {
        int i = 8;
        System.out.println(++i + 1);
        System.out.println(i++ + 1);

        /**
         * output:
         * 10
         * 10
         */
    }
}